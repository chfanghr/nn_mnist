auto main() -> int {
  Info("There is a big '5' in the original image");

  ShowStep(std::string("Loading network from ") + NETWORK_DAT_LOCATION);
  auto network = nn_mnist::Network(NETWORK_DAT_LOCATION);

  ShowStep("Evaluate the image");
  auto res = network.Evaluate(img);

  int max_idx = -1;
  double max{};

  ShowStep("Find result");
  for (size_t i = 0; i < res.size(); i++) {
	Info("Possibility of " + std::to_string(i) + " = " + std::to_string(res[i]));
	if (res[i] > max) {
	  max = res[i];
	  max_idx = i;
	}
  }

  if (max_idx >= 0)
	Info("The result is " + std::to_string(max_idx));
  else
	Panic("No result found");

  return EXIT_SUCCESS;
}